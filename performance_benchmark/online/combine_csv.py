#!/usr/bin/env python3
"""
Combine multiple benchmark CSV files generated by one script run into a single CSV.

CSV filename pattern (from vllm_random_benchmark_server.sh):
  {TIMESTAMP}_client_{DEV_TAG}{QUANT_TAG}{MTP_TAG}{MM_ITEMS}_{MM_W}x{MM_H}_tp{TP}_mbt{MBT}_in{INPUT}_out{OUTPUT}_{GPU}.csv

Usage:
  python3 combine_csv.py                          # combine all CSVs found
  python3 combine_csv.py --since 20260514_120000  # only CSVs from this run onward
  python3 combine_csv.py --output combine_myrun.csv
  python3 combine_csv.py --dir /path/to/online
"""

import sys
import os
import re
import glob
import csv
import argparse
from datetime import datetime


def parse_filename(filename):
    """
    Parse CSV filename to extract benchmark metadata.
    Returns (timestamp_str, meta_dict) or (None, None) on failure.
    """
    pattern = (
        r'^(\d{8}_\d{6})_client_'
        r'(?:dev\w+_)?'          # optional device tag: dev4_
        r'(fp8|fp16)_'           # quantization
        r'(nomtp|mtp)_'          # speculative decoding flag
        r'(\d+)_'                # images per request
        r'(\d+)x(\d+)_'         # WxH
        r'tp(\d+)_'              # tensor parallel
        r'mbt(\d+)_'             # max batched tokens
        r'in(\d+)_'              # input length
        r'out(\d+)_'             # output length
        r'(.+)\.csv$'            # GPU type
    )
    m = re.match(pattern, filename)
    if not m:
        return None, None

    ts, quant, mtp, mm_items, mm_w, mm_h, tp, mbt, input_len, output_len, gpu_type = m.groups()
    meta = {
        'timestamp': ts,
        'device': gpu_type,
        'precision': quant.upper(),
        'image_size': f'{mm_w}*{mm_h}',
        'images_per_request': int(mm_items),
        'input_len': int(input_len),
        'output_len': int(output_len),
    }
    return ts, meta


def simplify_model_name(model_dir):
    return model_dir.replace('-Instruct', '')


def main():
    parser = argparse.ArgumentParser(description='Combine benchmark CSV files into one.')
    parser.add_argument('--since', default=None,
                        help='Only include CSVs with timestamp >= YYYYMMDD_HHMMSS')
    parser.add_argument('--output', default=None,
                        help='Output filename (default: combine_{SINCE_or_NOW}.csv in --dir)')
    parser.add_argument('--dir', default='.',
                        help='Directory to search for */LOG/*_client_*.csv (default: .)')
    args = parser.parse_args()

    search_dir = args.dir
    pattern = os.path.join(search_dir, '*/LOG/*_client_*.csv')
    csv_files = sorted(glob.glob(pattern))

    if not csv_files:
        print(f"No CSV files found matching: {pattern}")
        sys.exit(0)

    if args.since:
        since_key = args.since[:15]
        csv_files = [f for f in csv_files if os.path.basename(f)[:15] >= since_key]

    if not csv_files:
        print(f"No CSV files found since {args.since}.")
        sys.exit(0)

    print(f"Found {len(csv_files)} CSV file(s) to combine.")

    combined_rows = []

    for csv_path in csv_files:
        norm_path = csv_path.replace('\\', '/')
        parts = norm_path.split('/')
        try:
            log_idx = next(i for i, p in enumerate(parts) if p == 'LOG')
        except StopIteration:
            print(f"  Skipping (no LOG dir in path): {csv_path}")
            continue

        model_dir = parts[log_idx - 1]
        model_name = simplify_model_name(model_dir)

        filename = os.path.basename(csv_path)
        ts, meta = parse_filename(filename)
        if meta is None:
            print(f"  Skipping (unrecognized filename format): {filename}")
            continue

        print(f"  Reading: {csv_path}")
        with open(csv_path, encoding='utf-8') as f:
            reader = csv.DictReader(f, skipinitialspace=True)
            for row in reader:
                if not any((v or '').strip() for v in row.values()):
                    continue
                combined_rows.append({
                    'Device':              meta['device'],
                    'Model':               model_name,
                    'Precision':           meta['precision'],
                    'image size':          meta['image_size'],
                    'images per request':  meta['images_per_request'],
                    'Input Text Length':   meta['input_len'],
                    'Output Length':       meta['output_len'],
                    'num_prompts':         (row.get('Batch Size') or '').strip(),
                    'TTFT (ms)':           (row.get('Mean TTFT (ms)') or '').strip(),
                    'TPOT (ms)':           (row.get('Mean TPOT (ms)') or '').strip(),
                    'TPS (tokens/s)':      (row.get('Output Token Throughput (tok/s)') or '').strip(),
                    'QPS (req/s)':         (row.get('Request Throughput (req/s)') or '').strip(),
                    'E2E Latency (s)':     (row.get('E2E Latency (s)') or '').strip(),
                })

    if not combined_rows:
        print("No data rows found in any CSV.")
        sys.exit(0)

    if args.output:
        output_path = args.output
    else:
        ts_tag = args.since or datetime.now().strftime('%Y%m%d_%H%M%S')
        output_path = os.path.join(search_dir, f'combine_{ts_tag}.csv')

    headers = [
        'Device', 'Model', 'Precision', 'image size', 'images per request',
        'Input Text Length', 'Output Length', 'num_prompts',
        'TTFT (ms)', 'TPOT (ms)', 'TPS (tokens/s)', 'QPS (req/s)', 'E2E Latency (s)',
    ]

    with open(output_path, 'w', newline='', encoding='utf-8') as f:
        writer = csv.DictWriter(f, fieldnames=headers)
        writer.writeheader()
        writer.writerows(combined_rows)

    print(f"\nCombined {len(combined_rows)} rows from {len(csv_files)} file(s) -> {output_path}")


if __name__ == '__main__':
    main()
